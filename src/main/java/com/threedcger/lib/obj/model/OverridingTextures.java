package com.threedcger.lib.obj.model;

public class OverridingTextures {
    private String metallicRoughnessOcclusionTexture;
    private String specularGlossinessTexture;
    private String occlusionTexture;
    private String normalTexture;
    private String baseColorTexture;
    private String emissiveTexture;
    private String alphaTexture;

    public String getMetallicRoughnessOcclusionTexture() {
        return metallicRoughnessOcclusionTexture;
    }

    public void setMetallicRoughnessOcclusionTexture(String metallicRoughnessOcclusionTexture) {
        this.metallicRoughnessOcclusionTexture = metallicRoughnessOcclusionTexture;
    }

    public String getSpecularGlossinessTexture() {
        return specularGlossinessTexture;
    }

    public void setSpecularGlossinessTexture(String specularGlossinessTexture) {
        this.specularGlossinessTexture = specularGlossinessTexture;
    }

    public String getOcclusionTexture() {
        return occlusionTexture;
    }

    public void setOcclusionTexture(String occlusionTexture) {
        this.occlusionTexture = occlusionTexture;
    }

    public String getNormalTexture() {
        return normalTexture;
    }

    public void setNormalTexture(String normalTexture) {
        this.normalTexture = normalTexture;
    }

    public String getBaseColorTexture() {
        return baseColorTexture;
    }

    public void setBaseColorTexture(String baseColorTexture) {
        this.baseColorTexture = baseColorTexture;
    }

    public String getEmissiveTexture() {
        return emissiveTexture;
    }

    public void setEmissiveTexture(String emissiveTexture) {
        this.emissiveTexture = emissiveTexture;
    }

    public String getAlphaTexture() {
        return alphaTexture;
    }

    public void setAlphaTexture(String alphaTexture) {
        this.alphaTexture = alphaTexture;
    }
}
